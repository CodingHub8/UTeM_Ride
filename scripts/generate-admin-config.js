const fs = require('fs');
const path = require('path');

const envPath = path.join(__dirname, '../.env');
const outputPath = path.join(__dirname, '../admin/env-config.js');

try {
  if (!fs.existsSync(envPath)) {
    console.warn('.env file not found at root directory. Skipping config generation.');
    process.exit(0);
  }

  const envContent = fs.readFileSync(envPath, 'utf8');
  const envVars = {};

  envContent.split('\n').forEach(line => {
    // Ignore comments and empty lines
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) return;

    const parts = trimmed.split('=');
    if (parts.length >= 2) {
      const key = parts[0].trim();
      const val = parts.slice(1).join('=').trim().replace(/^['"]|['"]$/g, ''); // strip optional quotes
      if (key.startsWith('EXPO_PUBLIC_FIREBASE_')) {
        envVars[key] = val;
      }
    }
  });

  const outputJs = `// Auto-generated by scripts/generate-admin-config.js\nwindow.process = {\n  env: ${JSON.stringify(envVars, null, 2)}\n};\n`;
  fs.writeFileSync(outputPath, outputJs, 'utf8');
  console.log('Successfully generated admin/env-config.js from .env variables.');
} catch (error) {
  console.error('Failed to generate admin config from .env:', error);
}
